import UIKit

extension UIColor {

    @nonobjc class var sixtOrange: UIColor? {
        return UIColor(named: "Sixt_Orange")
    }

    @nonobjc class var sixtGreen: UIColor? {
        return UIColor(named: "Sixt_Green")
    }

    @nonobjc class var sixtGray: UIColor? {
        return UIColor(named: "Sixt_Gray")
    }

    @nonobjc class var sixtBlack: UIColor? {
        return UIColor(named: "Sixt_Black")
    }

}
